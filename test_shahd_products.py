    p = find_product(rpc, input_name, brand='shahd')
    got = p['name'] if p else 'NOT FOUND'
    # تحقق من التطابق (normalized)
    def norm(s):
        return s.strip().replace('ة', 'ه').replace('ى', 'ي').replace('أ', 'ا').replace('إ', 'ا')
    
    if norm(got) == norm(expected):
        ok_count += 1
        print(f"✅ {input_name!r:35} -> {got!r}")
    else:
        errors.append((input_name, expected, got))
        print(f"❌ {input_name!r:35} -> {got!r}  (المتوقع: {expected!r})")

