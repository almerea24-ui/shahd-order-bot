def lookup_city_alias(city_name: str, province: str) -> str | None:
    """
    Look up a city alias for the given province.
    Priority: learned aliases > static aliases.
    Returns the exact Odoo city name if found, else None.
    """
    if not city_name or not province:
        return None

    # 1. Check learned aliases first (dynamic, from employee corrections)
    try:
        from city_learner import lookup_learned_alias
        learned = lookup_learned_alias(city_name, province)
        if learned:
            return learned
    except Exception:
        pass

    # 2. Check static aliases
    province_aliases = CITY_ALIASES_BY_PROVINCE.get(province, {})
    # Try exact match first
    if city_name in province_aliases:
        return province_aliases[city_name]
    # Try without ال التعريف
    city_stripped = city_name.lstrip("ال") if city_name.startswith("ال") else city_name
    for alias, odoo_name in province_aliases.items():
        alias_stripped = alias.lstrip("ال") if alias.startswith("ال") else alias
        if city_stripped == alias_stripped:
            return odoo_name
    return None
