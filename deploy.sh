#!/bin/bash
# ============================================================
# deploy.sh — نشر تحديثات البوت بضغطة واحدة
# الاستخدام: ./deploy.sh "وصف التحديث"
# مثال:    ./deploy.sh "إضافة منتج جديد للمسكارة"
# ============================================================

set -e  # وقف عند أي خطأ

export RAILWAY_TOKEN="8132f053-8361-4718-a873-3b42299a54bb"

MSG="${1:-update}"

echo ""
echo "🚀 جاري نشر التحديث: $MSG"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# 1. فحص الكود أولاً قبل أي شيء
echo "🔍 [1/4] فحص الكود..."
python3 test_bot.py
if [ $? -ne 0 ]; then
    echo "❌ الفحص فشل! لن يتم النشر حتى تُصلح الأخطاء."
    exit 1
fi

# 2. رفع على GitHub
echo ""
echo "📤 [2/4] رفع على GitHub..."
git add -A
git commit -m "$MSG" || echo "⚠️  لا يوجد تغييرات جديدة للرفع."
git push origin master

echo ""
echo "✅ [3/4] تم الرفع على GitHub بنجاح."

# 3. نشر على Railway
echo ""
echo "🚂 [3/4] نشر على Railway..."
railway up --detach

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
