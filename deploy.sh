#!/bin/bash
# ============================================================
# deploy.sh — نشر تحديثات البوت بضغطة واحدة
# الاستخدام: ./deploy.sh "وصف التحديث"
# مثال:    ./deploy.sh "إضافة منتج جديد للمسكارة"
# ============================================================

set -e  # وقف عند أي خطأ

MSG="${1:-update}"

echo ""
echo "🚀 جاري نشر التحديث: $MSG"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"

# 1. فحص الكود أولاً قبل أي شيء
echo "🔍 [1/4] فحص الكود..."
python3 test_bot.py
if [ $? -ne 0 ]; then
    echo "❌ الفحص فشل! لن يتم النشر حتى تُصلح الأخطاء."
    exit 1
fi

# 2. رفع على GitHub
echo ""
echo "📤 [2/4] رفع على GitHub..."
git add -A
git commit -m "$MSG" || echo "⚠️  لا يوجد تغييرات جديدة للرفع."
git push origin master

echo ""
echo "✅ [3/4] تم الرفع على GitHub بنجاح."

# 3. إرشادات Railway (إذا كانت مستخدمة)
echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "✅ [4/4] النشر مكتمل!"
echo ""
echo "📌 إذا كنت تستخدم Railway:"
echo "   → Railway سيكتشف التحديث تلقائياً ويعيد تشغيل البوت خلال دقيقة."
echo ""
echo "📌 إذا كنت تشغّل البوت على سيرفر يدوياً:"
echo "   → شغّل: git pull && python3 discord_bot.py"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
